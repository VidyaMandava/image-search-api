@app.post("/search", response_model=SearchResponse)
async def search_images(request: SearchRequest):
    """Search for images"""
    start_time = time.time()

    try:
        logger.info(f"üîç Searching for: {request.query}")

        # Perform search
        image_names = search_with_chromadb(request.query, request.max_results)

        # Build results
        results = []
        for i, image_name in enumerate(image_names):
            result = SearchResult(
                rank=i + 1,
                image_name=image_name,
                similarity_score=max(0.5, 1.0 - (i * 0.1)),
                image_url=f"/images/{image_name}"
            )

            if request.include_explanations:
                result.explanation = f"This image appears relevant to '{request.query}' based on visual similarity."

            results.append(result)

        processing_time = time.time() - start_time
        logger.info(f"‚úÖ Search completed in {processing_time:.2f}s")

        return SearchResponse(
            query=request.query,
            results=results,
            total_results=len(results),
            processing_time=processing_time
        )

    except Exception as e:
        logger.error(f"‚ùå Search error: {str(e)}")
        raise HTTPException(status_code=500, detail=str(e))

